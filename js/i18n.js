export function setLanguage(language) {
  currentLanguage = STRINGS[language] ? language : DEFAULT_LANGUAGE;
}

export function getLanguage() {
  return currentLanguage;
}

export function t(key, params = {}) {
  const template = STRINGS[currentLanguage]?.[key] ?? STRINGS[DEFAULT_LANGUAGE]?.[key] ?? key;
  return template.replace(/\{(\w+)\}/g, (_, name) => String(params[name] ?? ""));
}

export function translateTrend(value) {
  return t(`trend.${value || "insufficient"}`);
}

export function languageOptions() {
  return [
    { value: "nl", label: "NL" },
    { value: "en", label: "ENG" },
  ];
}
